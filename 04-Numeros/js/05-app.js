// Incremento / decremento

let score = 5;

// INCREMENTO EN 1
// Se llama primero a puntaje y luego se suma
score++;

// Se suma primero y luego se asigna a la variable
++score;

// DECREMENTO EN 1
score--;

--score;

// INCREMENTO EN MÁS DE 1
score += 3;

// DECREMENTO EN MÁS DE 1
score -= 3;

console.log(score);