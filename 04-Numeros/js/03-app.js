let result;

// PI
result = Math.PI;

// Redondear
result = Math.floor(2.8);
result = Math.ceil(2.5);
result = Math.round(2.6);

// Raíz cuadrada
result = Math.sqrt(144);

// Valor absoluto
result = Math.abs(-500);

// Potencia
result = Math.pow(2, 4);

// Mínimo
result = Math.min(5, 8, 12, 3, 2, 11);

// Máximo
result = Math.max(5, 6, 2, 9, 12, 35, 1);

// Aleatorio
result = Math.random();

// Aleatorio dentro de un rango (0 - 30)
result = Math.floor(Math.random() * 30);

